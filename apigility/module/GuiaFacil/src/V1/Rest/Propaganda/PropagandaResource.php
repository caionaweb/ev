<?php
namespace GuiaFacil\V1\Rest\Propaganda;

use ZF\ApiProblem\ApiProblem;
use ZF\Rest\AbstractResourceListener;

class PropagandaResource extends AbstractResourceListener
{
    protected $mappeer;

    public function __construct($mapper)
    {
        $this->mappeer = $mapper;
    }

    /**
     * Create a resource
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function create($data)
    {
        $propaganda = new PropagandaEntity();

        $propaganda->id_empresa = $data->id_empresa;
        $propaganda->imagem = $data->imagem;

        return $this->mappeer->insere($propaganda);
    }

    /**
     * Delete a resource
     *
     * @param  mixed $id
     * @return ApiProblem|mixed
     */
    public function delete($id)
    {
        return new ApiProblem(405, 'The DELETE method has not been defined for individual resources');
    }

    /**
     * Delete a collection, or members of a collection
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function deleteList($data)
    {
        return new ApiProblem(405, 'The DELETE method has not been defined for collections');
    }

    /**
     * Fetch a resource
     *
     * @param  mixed $id
     * @return ApiProblem|mixed
     */
    public function fetch($id_empresa)
    {
        return $this->mappeer->fetchUm($id_empresa);
    }

    /**
     * Fetch all or a subset of resources
     *
     * @param  array $params
     * @return ApiProblem|mixed
     */
    public function fetchAll($params = [])
    {
        return $this->mappeer->fetchAll();
    }

    /**
     * Patch (partial in-place update) a resource
     *
     * @param  mixed $id
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function patch($id, $data)
    {
        return new ApiProblem(405, 'The PATCH method has not been defined for individual resources');
    }

    /**
     * Patch (partial in-place update) a collection or members of a collection
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function patchList($data)
    {
        return new ApiProblem(405, 'The PATCH method has not been defined for collections');
    }

    /**
     * Replace a collection or members of a collection
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function replaceList($data)
    {
        return new ApiProblem(405, 'The PUT method has not been defined for collections');
    }

    /**
     * Update a resource
     *
     * @param  mixed $id
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function update($id_empresa, $data)
    {
        $propaganda = new PropagandaEntity();

        $propaganda->id_propaganda = $data->id_propaganda;
        $propaganda->id_empresa = $id_empresa;
        $propaganda->imagem = $data->imagem;
        $propaganda->delete = $data->delete;

        return $this->mappeer->update($propaganda);

    }
}
